package CompanyInfo;

public class CompanyInfo {
	private String compangName;
	private String companyBoss;
	private String companyAddress;
	private int companyTelephoneNumber;
	private String companyEmail;
	public String getCompanyName() {
		return compangName;
	}
	public void setCompanyName(String compangName) {
		this.compangName = compangName;
	}
	public String getCompanyBoss() {
		return companyBoss;
	}
	public void setCompanyBoss(String companyBoss) {
		this.companyBoss = companyBoss;
	}
	public String getCompanyAddress() {
		return companyAddress;
	}
	public void setCompanyAddress(String companyAddress) {
		this.companyAddress = companyAddress;
	}
	public int getCompanyTelephoneNumber() {
		return companyTelephoneNumber;
	}
	public void setCompanyTelephoneNumber(int companyTelephoneNumber) {
		this.companyTelephoneNumber = companyTelephoneNumber;
	}
	public String getCompanyEmail() {
		return companyEmail;
	}
	public void setCompanyEmail(String companyEmail) {
		this.companyEmail = companyEmail;
	}
	
	

	
	

}
