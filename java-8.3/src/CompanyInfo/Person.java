package CompanyInfo;

public class Person {
	
	private String name;
	private int age;
	private String email;
	private Adds[] adds;	
	public Adds[] getAdds() {
		return adds;
	}
	public void setAdds(Adds[] adds) {
		this.adds = adds;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	

}
