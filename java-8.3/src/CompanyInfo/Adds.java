package CompanyInfo;

public class Adds {
	private String address;
	private int phone;
	
	
	public Adds(String address, int phone) {
		this.address = address;
		this.phone = phone;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public int getPhone() {
		return phone;
	}
	public void setPhone(int phone) {
		this.phone = phone;
	}
	

}
